package Section2;

public class WholeNumbers{
    public static void main(String[] args) {
        int numberOfFans = 5000;
        System.out.println("There are " + numberOfFans + " at the Quidditch Games");
        
        // Int can't hold values greater than 2 billion
        long globalPopulation = 7000000000L;
        System.out.println("There are " + globalPopulation + " people on earth");

        long dailyGoogleSearches = 50000000L;
        System.out.println("There are " + dailyGoogleSearches + " google searches better");
    }
}